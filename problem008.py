"""
Find the greatest product of five consecutive digits in the 1000-digit number.

From http://projecteuler.net/index.php?section=problems&id=8
"""

def problem008(n, number):
    digits = map(int, list(number))
    max = 0
    for i in range(len(digits) - (n - 1)):
        product = (digits[i] * digits[i + 1] * digits[i + 2]
            * digits[i + 3] * digits[i + 4])
        if product > max:
            max = product
    return max

